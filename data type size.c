#include<stdio.h>
int main()
{
    int i;
    float f;
    double d;
    char c;

    printf("Size of integer : %d byte\n",sizeof(i));

    printf("Size of float : %d byte\n",sizeof(f));

    printf("Size of double : %d byte\n",sizeof(d));

    printf("Size of char : %d byte\n",sizeof(c));

    return 0;

}
